const DEFAULT_LANGUAGE = "zh-CN";
const FALLBACK_LANGUAGE = "en-US";

export let currentLanguage = DEFAULT_LANGUAGE;

export function hasLanguage(language) {
  return Object.prototype.hasOwnProperty.call(translations, language);
}

export function setLanguage(language) {
  currentLanguage = hasLanguage(language) ? language : DEFAULT_LANGUAGE;
  return currentLanguage;
}

export function translate(key, params = {}) {
  let text = translations[currentLanguage]?.[key] ?? translations[FALLBACK_LANGUAGE]?.[key] ?? key;
  Object.entries(params).forEach(([name, value]) => {
    text = text.replace(`{${name}}`, value);
  });
  return text;
}
